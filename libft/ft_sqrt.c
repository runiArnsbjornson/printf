#include "libft.h"

int		ft_sqrt(int n)
{
	size_t i;
	size_t nb;

	if (n <= 0)
		return (0);
	if (n == 1)
		return (1);
	nb = n;
	i = 2;
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
